package main

import "fmt"

func main() {
	//การดำเนินการทางคณิตศาสตร์
	fNumber := 60
	fSecond := 5

	fmt.Println(fNumber + fSecond)
	fmt.Println(fNumber - fSecond)
	fmt.Println(fNumber * fSecond)
	fmt.Println(fNumber / fSecond)

	//ชุดข้อความ
	p1 := "Leonadon"
	p2 := "Tutorial"

	//Concatenation
	p3 := p1 + p2
	fmt.Println(p3)
	fmt.Println(p3[0])
}
